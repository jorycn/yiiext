class ActiveRecord extends CActiveRecord{
//----------------------------------------------------------------------------//
	// Экранирование спецсимволов
	function htmlEncode($val){
		return CHtml::encode($val);
	}	
//----------------------------------------------------------------------------//
	// Упрощениее сохранения в БД
	public 	function add($data){
		if (isset($data)){
			$this->attributes = $data;
			return $this->save();
		}
	}	
//----------------------------------------------------------------------------//
}
